public class WhileDo {

    public static void main(String[] args) {
/*

While -->

* */
        int number = 0;

        while (number < 7){
            System.out.println(number);
            number++;
        }
/*

Do While -->

* */
        do{
            System.out.println(number);
            number++;
        }while(number < 7);
    }
}
